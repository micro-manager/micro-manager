    private void jbOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbOKActionPerformed
        // Save vals and close
        try {
            lc.deviceName = jtfDeviceName.getText();
            lc.propertyName = jtfPropertyName.getText();
            lc.maxValue = Double.valueOf(jtfMaxVoltage.getText());
            lc.minStep = Double.valueOf(jtfMinStep.getText());
            lc.minValue = Double.valueOf(jtfMinVoltage.getText());
            lc.startValue = Double.valueOf(jtfStartVoltage.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Make sure Vmin, Vmax, Vstart and min step are numbers");
        }
        
        setVisible(false);
    }//GEN-LAST:event_jbOKActionPerformed

